public class Common_Unit {
    private String unit;
    private float value;

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "Common_Unit{" + "unit=" + unit + ", value=" + value + '}';
    }
    
    
}
