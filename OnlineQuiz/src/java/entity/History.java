package entity;

/**
 *
 * @author hp
 */
public class History {
    private float score;
    private int status, idUser;
    private String date;

    public History() {
    }

    public History(float score, int status, String date, int idUser) {
        this.score = score;
        this.status = status;
        this.idUser = idUser;
        this.date = date;
    }

    public float getScore() {
        return score;
    }

    public void setScore(float score) {
        this.score = score;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    
}
